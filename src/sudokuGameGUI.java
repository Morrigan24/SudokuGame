        for(int row=0; row<9; row+=3){
            for(int col=0; col<9; col+=3){
                
                for(int pos=0; pos<8; pos++){
                    for(int pos2 = pos + 1; pos2<9; pos2++){
                        if(values[row + pos%3][col + pos/3] == values[row + pos2%3][col + pos2/3] && values[row][col] != 0){
                            return false;
                        }
                    }
                }
            }
        }
        // verifies 3x3 matrices
        
        return true;
