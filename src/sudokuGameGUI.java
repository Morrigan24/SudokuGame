    private void checkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkButtonActionPerformed
        // check counts
        String helpNumber = checkNo.getText();
        if(helpNumber.equals("4")){
            checkNo.setText("3");
        }
        if(helpNumber.equals("3")){
            checkNo.setText("2");
        }
        if(helpNumber.equals("2")){
            checkNo.setText("1");
        }
        if(helpNumber.equals("1")){
            checkNo.setText("0");
            checkButton.setEnabled(false);
        }
        
        
        // validation method calling
        if(validation(boxes) == true){
            JOptionPane.showMessageDialog(rootPane, "Everything's alright");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "There's an error!");
        }
        
        int count =0;
        for(int i=0;i<9;i++){
            for(int j=0;j<9;j++){
                if(!boxes[i][j].getText().equals("0")){
                    count++;
                }
                    if(count == 81){
                        if(validation(boxes) == true){
                            JOptionPane.showMessageDialog(rootPane, "You won!!");
                            validating = true;
                        }
                }
            }
        }
        if(validating == true){
            restart.doClick();
        }
        
    }//GEN-LAST:event_checkButtonActionPerformed

